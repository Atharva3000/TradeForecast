# Flat lookup:  "Display Name" → "Ticker"
ALL_ASSETS: dict[str, str] = {}
for _cat_assets in CATEGORIES.values():
    ALL_ASSETS.update(_cat_assets)

# Reverse lookup:  "Ticker" → "Display Name"
TICKER_TO_NAME: dict[str, str] = {v: k for k, v in ALL_ASSETS.items()}


def search_assets(query: str, category: str | None = None) -> list[str]:
    """
    Fuzzy-search asset names.  Returns display names matching *query*.
    If *category* is provided, restricts to that category.
    """
    pool = CATEGORIES.get(category, ALL_ASSETS) if category else ALL_ASSETS
    q = query.lower()
    return [name for name in pool if q in name.lower() or q in pool[name].lower()]


def resolve_ticker(display_name: str) -> str | None:
    """Convert a display name to its yFinance ticker. Returns None if not found."""
    return ALL_ASSETS.get(display_name)


def get_category_names() -> list[str]:
    """Return ordered list of category names."""
    return list(CATEGORIES.keys())


def get_assets_for_category(category: str) -> list[str]:
    """Return sorted display names for a given category."""
    assets = CATEGORIES.get(category, {})
    return sorted(assets.keys())
